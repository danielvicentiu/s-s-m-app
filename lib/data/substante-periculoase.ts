/**
 * Funcție helper pentru căutare după CAS Number
 */
export function getSubstantaByCAS(casNumber: string): SubstantaPericuloasa | undefined {
  return substantePericuloase.find(s => s.casNumber === casNumber);
}

/**
 * Funcție helper pentru filtrare după clasă de pericol
 */
export function getSubstanteByHazardClass(hazardClass: string): SubstantaPericuloasa[] {
  return substantePericuloase.filter(s =>
    s.hazardClass.some(h => h.toLowerCase().includes(hazardClass.toLowerCase()))
  );
}

/**
 * Funcție helper pentru substanțe cu OEL sub o anumită valoare
 */
export function getSubstanteHighRisk(oelThreshold: number = 10): SubstantaPericuloasa[] {
  return substantePericuloase.filter(s => s.OEL_mg_m3 !== null && s.OEL_mg_m3 < oelThreshold);
}
