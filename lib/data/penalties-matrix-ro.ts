/**
 * Helper functions for filtering penalties
 */
export const getPenaltiesByDomain = (domain: Penalty['domain']): Penalty[] =>
  penaltiesMatrixRO.filter((p) => p.domain === domain);

export const getPenaltiesBySeverity = (severity: Penalty['severity']): Penalty[] =>
  penaltiesMatrixRO.filter((p) => p.severity === severity);

export const getPenaltiesByAuthority = (
  authority: Penalty['inspectorateAuthority']
): Penalty[] => penaltiesMatrixRO.filter((p) => p.inspectorateAuthority === authority);

export const searchPenalties = (searchTerm: string): Penalty[] => {
  const term = searchTerm.toLowerCase();
  return penaltiesMatrixRO.filter(
    (p) =>
      p.violation.toLowerCase().includes(term) ||
      p.legalBasis.toLowerCase().includes(term) ||
      p.id.toLowerCase().includes(term)
  );
};

export const getCriticalPenalties = (): Penalty[] =>
  penaltiesMatrixRO.filter((p) => p.canSuspend || p.severity === 'critical');

