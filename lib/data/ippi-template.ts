/**
 * Helper function to generate complete IPPI document as formatted text
 */
export function generateIppiDocument(organizationName?: string): string {
  let document = "";

  if (organizationName) {
    document += `${organizationName}\n\n`;
  }

  document += `${ippiTemplate.title}\n`;
  document += "=".repeat(ippiTemplate.title.length) + "\n\n";

  ippiTemplate.sections.forEach((section) => {
    document += `${section.title}\n`;
    document += "-".repeat(section.title.length) + "\n\n";

    if (Array.isArray(section.content)) {
      section.content.forEach((paragraph) => {
        document += `${paragraph}\n\n`;
      });
    } else {
      document += `${section.content}\n\n`;
    }
  });

  return document;
}

/**
 * Helper function to get section by title
 */
export function getIppiSection(sectionTitle: string): IppiSection | undefined {
  return ippiTemplate.sections.find(
    (section) => section.title.toLowerCase().includes(sectionTitle.toLowerCase())
  );
}

/**
 * Helper function to get all section titles
 */
export function getIppiSectionTitles(): string[] {
  return ippiTemplate.sections.map((section) => section.title);
}
