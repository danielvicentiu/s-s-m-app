/**
 * Funcție utilă pentru filtrare după categorie
 */
export const getFactoriByCategory = (category: RiskCategory): RiskFactor[] => {
  return factoriRisc.filter((factor) => factor.category === category);
};

/**
 * Funcție utilă pentru căutare după ID
 */
export const getFactorById = (id: string): RiskFactor | undefined => {
  return factoriRisc.find((factor) => factor.id === id);
};

/**
 * Funcție utilă pentru căutare text
 */
export const searchFactori = (query: string): RiskFactor[] => {
  const lowerQuery = query.toLowerCase();
  return factoriRisc.filter(
    (factor) =>
      factor.name.toLowerCase().includes(lowerQuery) ||
      factor.controlMeasures.some((measure) => measure.toLowerCase().includes(lowerQuery))
  );
};
