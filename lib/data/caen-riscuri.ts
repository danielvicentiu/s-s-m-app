/**
 * Helper function to get CAEN risk data by code
 */
export function getCaenRiskByCode(code: string): CaenRisk | undefined {
  return caenRiscuri.find((item) => item.code === code);
}

/**
 * Helper function to get all CAEN codes by risk level
 */
export function getCaenByRiskLevel(riskLevel: RiskLevel): CaenRisk[] {
  return caenRiscuri.filter((item) => item.riskLevel === riskLevel);
}

/**
 * Helper function to search CAEN codes by name (case-insensitive)
 */
export function searchCaenByName(searchTerm: string): CaenRisk[] {
  const lowerSearch = searchTerm.toLowerCase();
  return caenRiscuri.filter((item) =>
    item.name.toLowerCase().includes(lowerSearch)
  );
}
