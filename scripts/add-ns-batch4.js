const locales = ['ro', 'en', 'bg', 'hu', 'de', 'pl'];
let totalAdded = 0;

for (const locale of locales) {
  const filePath = path.join(messagesDir, `${locale}.json`);
  const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const added = [];

  if (!existing.obligations) {
    existing.obligations = obligations[locale];
    added.push('obligations');
  } else {
    console.log(`  [SKIP] ${locale}.json already has "obligations"`);
  }

  if (!existing.profile) {
    existing.profile = profile[locale];
    added.push('profile');
  } else {
    console.log(`  [SKIP] ${locale}.json already has "profile"`);
  }

  if (!existing.psi) {
    existing.psi = psi[locale];
    added.push('psi');
  } else {
    console.log(`  [SKIP] ${locale}.json already has "psi"`);
  }

  fs.writeFileSync(filePath, JSON.stringify(existing, null, 2) + '\n', 'utf8');

  if (added.length > 0) {
    console.log(`✓ ${locale}.json — added: ${added.join(', ')}`);
    totalAdded += added.length;
  } else {
    console.log(`  ${locale}.json — nothing new to add`);
  }
}

console.log(`\nDone. Total namespaces added: ${totalAdded}`);
